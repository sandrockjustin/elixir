
# These are mostly unnecessary given their simplicity,but this is just to practice
defmodule BasicArithmetic do

  def add(x, y) do
    x + y
  end

  def subtract(x, y) do
    x - y
  end

  def multiply(x, y) do
    x * y
  end

  def divide_int(x, y) do
    div(x, y)
  end

  def divide_float(x, y) do
    x / y
  end

  def remainder(x, y) do
    rem(x, y)
  end

end
